package everbrite.model;

/**
 *
 * @author Whitehurst
 */
public class Material {
    
    private String name;
    private String type;
    private String desc;
    private double cost;
    private boolean stocked;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getDesc() {
        return desc;
    }

    public double getCost() {
        return cost;
    }

    public boolean isStocked() {
        return stocked;
    }
    
    
    
}
